#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	int				slen;
	char			*res;

	if (!s || !f)
		return (0);
	slen = ft_strlen(s);
	i = 0;
	res = malloc((slen + 1) * sizeof(char));
	if (!res)
		return (0);
	while (s[i])
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = 0;
	return (res);
}
